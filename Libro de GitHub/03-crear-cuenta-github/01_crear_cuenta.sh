# =============================================================================
# ARCHIVO: 01_crear_cuenta.sh
# TEMA: Crear tu cuenta en GitHub — Paso a paso
# =============================================================================
#
# Crear una cuenta en GitHub es un proceso que se hace desde el navegador web.
# Este archivo te guía paso a paso con instrucciones detalladas.
#
# NOTA: Como este proceso es visual (en el navegador), este archivo contiene
# instrucciones como comentarios. No hay comandos para ejecutar aquí.
# =============================================================================


# ─────────────────────────────────────────────────────────────────────────────
# PASO 1: Ir a GitHub
# ─────────────────────────────────────────────────────────────────────────────
# Abre tu navegador web y ve a:
#   https://github.com
#
# Verás la página principal de GitHub con un botón "Sign up" (Registrarse)


# ─────────────────────────────────────────────────────────────────────────────
# PASO 2: Hacer clic en "Sign up"
# ─────────────────────────────────────────────────────────────────────────────
# Haz clic en el botón verde "Sign up" en la esquina superior derecha
# Se abrirá un formulario de registro paso a paso


# ─────────────────────────────────────────────────────────────────────────────
# PASO 3: Ingresar tu correo electrónico
# ─────────────────────────────────────────────────────────────────────────────
# Escribe el MISMO correo electrónico que configuraste en Git.
# Esto es importante para que GitHub vincule tus commits con tu cuenta.
#
# Ejemplo: tu.correo@gmail.com
#
# CONSEJO: Usa un correo que revises frecuentemente.
# GitHub te enviará notificaciones sobre tus repositorios.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 4: Crear una contraseña
# ─────────────────────────────────────────────────────────────────────────────
# Crea una contraseña segura. GitHub requiere al menos:
#   - 8 caracteres como mínimo
#   - Al menos un número
#   - Al menos una letra minúscula
#
# CONSEJO: Usa un gestor de contraseñas (como Bitwarden o 1Password)
# para generar y guardar una contraseña fuerte.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 5: Elegir tu nombre de usuario (username)
# ─────────────────────────────────────────────────────────────────────────────
# Tu username es tu identidad pública en GitHub.
# Aparecerá en la URL de tu perfil: https://github.com/tu-username
#
# CONSEJOS para elegir un buen username:
#   ✅ Usa algo profesional y reconocible
#   ✅ Preferiblemente tu nombre real o una variación
#   ✅ Corto y fácil de recordar
#   ❌ Evita nombres con números aleatorios (usuario12345)
#   ❌ Evita nombres ofensivos o poco profesionales
#   ❌ Evita nombres muy largos o difíciles de escribir
#
# Ejemplos buenos: "juanperez", "maria-dev", "carlosgarcia"
# Ejemplos malos: "xXcoder_2005Xx", "asdf1234", "elprogramador420"


# ─────────────────────────────────────────────────────────────────────────────
# PASO 6: Preferencias de email
# ─────────────────────────────────────────────────────────────────────────────
# GitHub te preguntará si quieres recibir actualizaciones por correo.
# Puedes seleccionar "n" (no) si no quieres recibir correos de marketing.
# Siempre recibirás las notificaciones importantes de tus repos.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 7: Verificar tu cuenta
# ─────────────────────────────────────────────────────────────────────────────
# GitHub te pedirá resolver un CAPTCHA (un puzzle visual) para verificar
# que eres una persona real y no un bot.
# Simplemente sigue las instrucciones del puzzle.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 8: Código de verificación por email
# ─────────────────────────────────────────────────────────────────────────────
# GitHub enviará un código numérico a tu correo electrónico.
# Abre tu correo, busca el email de GitHub y escribe el código.
#
# Si no lo ves en la bandeja de entrada, revisa la carpeta de Spam.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 9: Personalización inicial
# ─────────────────────────────────────────────────────────────────────────────
# GitHub te hará algunas preguntas sobre ti:
#   - ¿Cuántas personas hay en tu equipo?  → "Just me" (Solo yo)
#   - ¿Eres estudiante o profesor?  → Selecciona lo que aplique
#   - ¿Qué funciones te interesan?  → Puedes saltar esto
#
# Estas respuestas son opcionales y puedes cambiarlas después.


# ─────────────────────────────────────────────────────────────────────────────
# PASO 10: Seleccionar plan
# ─────────────────────────────────────────────────────────────────────────────
# GitHub te mostrará los planes disponibles:
#   - Free → GRATIS. Es todo lo que necesitas para este curso.
#   - Pro → $4/mes. Funciones adicionales para profesionales.
#   - Team → Para equipos. No lo necesitas ahora.
#   - Enterprise → Para empresas grandes. No lo necesitas.
#
# Selecciona "Free" y continúa. El plan gratuito incluye:
#   ✅ Repositorios públicos y privados ilimitados
#   ✅ GitHub Actions (2000 minutos/mes)
#   ✅ GitHub Pages
#   ✅ Colaboración con otros usuarios


# ─────────────────────────────────────────────────────────────────────────────
# ¡LISTO! Tu cuenta de GitHub está creada
# ─────────────────────────────────────────────────────────────────────────────
# Ahora puedes acceder a tu perfil en:
#   https://github.com/tu-username
#
# En el siguiente archivo aprenderás a configurar tu perfil profesionalmente.


# =============================================================================
# RESUMEN:
# 1. Ve a https://github.com y haz clic en "Sign up"
# 2. Ingresa tu correo (el mismo que usaste en Git)
# 3. Crea una contraseña segura
# 4. Elige un username profesional
# 5. Verifica tu cuenta con el código enviado a tu correo
# 6. Selecciona el plan "Free"
# 7. ¡Tu cuenta está lista!
#
# Siguiente archivo: 02_configurar_perfil.sh
